package com.example.a.myapplication12321321.base.recyclerviewadapter;

public class JayChou {

    private String date;
    private String seat;
    private String imgurl;

    public JayChou(String date, String seat, String imgurl) {
        this.date = date;
        this.seat = seat;
        this.imgurl = imgurl;
    }

    public String getDate() {
        return date;
    }

    public String getSeat() {
        return seat;
    }

    public String getImgurl() {
        return imgurl;
    }
}
